class ControllerIndex extends Controller {
	public function index() {
	
		$this->document->setTitle("PHP Minimal MVC Project");
		$this->document->setDescription("Welcome to PHP Minimal MVC Project");
		$this->document->setKeywords("Page Keywords");
		$data['header'] = $this->load->controller('header');

		$data['welcome'] = "Welcome to PHP Minimal MVC Project";
		$this->response->setOutput($this->load->view('index', $data));
	}
